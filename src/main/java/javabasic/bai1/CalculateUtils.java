package javabasic.bai1;

public class CalculateUtils {
	public static void tinhCong(double x, double y) {
		System.out.print(x+y);
	};
	public static void tinhTru(double x, double y) {
		System.out.print(x-y);
	};
	public static void tinhNhan(double x, double y) {
		System.out.print(x*y);
	};
	public static void tinhChia(double x, double y) {
		System.out.print(x/y);
	};
}
